public class Ex07 {

	public static void main(String[] args) {
		// 과제 [순서도   1~100 홀수의 합 ]
		// for   순서도
		// while 순서도
		
		// ASCII + for 문 출력.
		// 256가지 문자
		// 0~31  제어문자    '\n'   '\t'   등등
		// 32~127 일반문자 ( 대65~90 소97~122 숫48~57 )
		// 128~
		for (int i = 0; i < 256; i++) {
			System.out.printf("[%d('%c')]\n", i, (char)i);
		} // for i
		

	} // main

} // class
